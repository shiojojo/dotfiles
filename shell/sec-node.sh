#!/bin/sh
# ============================================================
# Node.js セキュリティ設定 (Harness Engineering)
#
# 目的: サプライチェーン攻撃対策として使い捨てコードの直接実行をブロックし、
#       依存管理とタスク実行を pnpm に完全一元化する。
# 読込: ~/dotfiles/shell/sec-*.sh として Mac/Linux 共通でロード
# ============================================================

# ------------------------------------------------------------
# 1. npm の全面無効化 (pnpm への完全一元化とAIへの誘導)
# ------------------------------------------------------------
# AIエージェントが自律的にエラーを読み取り、pnpmへ切り替えて
# 再実行できるように、明確な変換例と解決策(corepack)を提示します。
npm() {
  echo "❌ [Harness Guard] npm コマンドの使用は全面的にブロックされています。" >&2
  echo "💡 理由: プロジェクトのタスク実行および依存管理は pnpm に完全一元化されています。" >&2
  echo "✅ 対策: AIエージェントおよびユーザーは、npm の代わりに pnpm を使用してください。" >&2
  echo "   - 変換例: npm install -> pnpm install" >&2
  echo "   - 変換例: npm run dev -> pnpm dev" >&2
  echo "   - 変換例: npm test    -> pnpm test" >&2
  echo "   (※ pnpm が未インストールの場合は corepack enable pnpm を実行してください)" >&2
  return 1
}

# ------------------------------------------------------------
# 2. npx の無効化 (直接実行 / 使い捨て実行の防止)
# ------------------------------------------------------------
# リモートから未検疫のパッケージを直接実行する危険な挙動を防ぎます。
# 一度ローカルプロジェクトに固定(add -D)させることで、ロックファイルによる
# 依存関係の可視化と検疫を強制します。
npx() {
  echo "🚨 [Harness Guard] npx によるリモートコードの直接実行はブロックされました。" >&2
  echo "💡 理由: 未検疫パッケージの使い捨て実行や、typoスクワッティングを防ぐためです。" >&2
  echo "" >&2
  echo "✅ 対策: プロジェクトに固定してから実行してください:" >&2
  echo "     1. pnpm add -D <package>  # 依存関係としてプロジェクトに追加" >&2
  echo "     2. pnpm exec <package>    # ローカルの実行ファイルとして起動" >&2
  return 1
}

# ------------------------------------------------------------
# 3. pnpx の無効化 (同上の理由)
# ------------------------------------------------------------
# npx と同様の理由で pnpx の使用もブロックします。
# 実行する場合は pnpm exec を明示的に使用させます。
pnpx() {
  echo "🚨 [Harness Guard] pnpx による直接実行はブロックされました。" >&2
  echo "💡 理由: pnpm のエコシステムにおいても、使い捨て実行は許可されていません。" >&2
  echo "✅ 対策: pnpm add -D <package> でインストール後、pnpm exec を使用してください。" >&2
  return 1
}

# ------------------------------------------------------------
# 4. pnpm dlx の無効化 (npx 相当・現行の使い捨て実行コマンド)
# ------------------------------------------------------------
# pnpm v8 以降 pnpx の後継として導入された使い捨て実行コマンド。
# npx と同様にロックファイルに記録されず、実行履歴が残らないためブロック。
# pnpx をブロックしても pnpm dlx が通れば意味が薄れるため必須。
pnpm() {
  if [ "$1" = "dlx" ]; then
    echo "🚨 [Harness Guard] pnpm dlx によるリモートコードの直接実行はブロックされました。" >&2
    echo "💡 理由: lockfile に記録されず実行履歴が残らない使い捨て実行は許可されていません。" >&2
    echo "" >&2
    echo "✅ 対策: プロジェクトに固定してから実行してください:" >&2
    echo "     1. pnpm add -D <package>  # 依存関係としてプロジェクトに追加" >&2
    echo "     2. pnpm exec <package>    # ローカルの実行ファイルとして起動" >&2
    return 1
  fi
  command pnpm "$@"
}