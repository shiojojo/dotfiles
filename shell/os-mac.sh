#!/bin/sh
# ============================================================
# macOS 固有セキュリティ設定 (Harness Engineering)
#
# 目的: Homebrew の勝手な挙動の制限と、macOS標準シェル(zsh)の
#       履歴を通じた機密情報漏洩を防止する。
# 読込: ~/dotfiles/setup.sh により OS 判定後にロード
# ============================================================

# ------------------------------------------------------------
# 1. Homebrew 防衛設定
# ------------------------------------------------------------

# インストール前の自動アップデートを禁止 (時間差検疫の徹底)
# brew install 時に Homebrew 本体と全 formula が勝手に更新され、
# 既存の依存関係が破壊される事故を防ぐ。
export HOMEBREW_NO_AUTO_UPDATE=1

# Cask インストール時に SHA256 チェックサムを必須化
# チェックサムのない野良 Cask のインストールを拒否し、
# 配信サーバー改ざんによるマルウェア混入を防ぐ。
export HOMEBREW_CASK_OPTS="--require-sha"

# HTTP リダイレクトの禁止
# ダウンロード時の中間者攻撃 (MITM) や、安全でないミラーサイトへの
# リダイレクトを強制的にブロックする。
export HOMEBREW_NO_INSECURE_REDIRECT=1

# アナリティクス(利用統計)の送信を無効化
# 意図しない環境情報の外部送信を遮断する。
export HOMEBREW_NO_ANALYTICS=1

# ------------------------------------------------------------
# 2. シェル履歴の防衛設定 (zsh専用)
# ------------------------------------------------------------
# AIエージェントや悪意あるプロセスが履歴ファイル(~/.zsh_history)から
# 機密情報を読み取るリスクを軽減する。

# スペース始まりのコマンドを履歴に保存しない
setopt HIST_IGNORE_SPACE

# 機密情報を含む可能性のあるコマンドを履歴から除外
export HISTORY_IGNORE="(*TOKEN*|*SECRET*|*PASSWORD*|*KEY*|*API_KEY*|*AUTH*)"

# ------------------------------------------------------------
# 3. Mac 固有のエイリアス・補助コマンド
# ------------------------------------------------------------
# 例: 意図的に Homebrew を手動更新する時用の安全なエイリアス
alias brew-safe-upgrade="brew update && brew upgrade"