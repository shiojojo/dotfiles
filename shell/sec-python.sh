#!/bin/sh
# ============================================================
# Python セキュリティ設定 (Harness Engineering)
#
# 目的: サプライチェーン攻撃対策として pip / uvx を無効化し、
#       依存管理とタスク実行を uv に完全一元化する。
# 読込: ~/dotfiles/shell/sec-*.sh として Mac/Linux 共通でロード
# ============================================================

# ------------------------------------------------------------
# 1. 仮想環境の強制 (環境汚染防止)
# ------------------------------------------------------------
# グローバル Python 環境への直接インストールを禁止する。
# uv は仮想環境を自動管理するため、pip フォールバックを
# 防ぐ二重の安全装置として機能する。
export PIP_REQUIRE_VIRTUALENV=true

# OS が使っている Python 環境 (OSシステムPython) の書き換えを禁止
# uv が誤ってシステム Python を上書きするリスクを完全ブロック
export UV_SYSTEM_PYTHON=false

# パッケージキャッシュのリンク方法をコピーに強制
# [無効化] 理由: verify-store-integrity が先に機能すべきであり、
# 実益より速度低下・ディスク使用量増加のコストが上回るため。
# 再有効化する場合は uv.toml の整合性検証との優先度を整理すること。
# export UV_LINK_MODE=copy

# ------------------------------------------------------------
# 2. pip / pip3 の全面無効化 (AIへの誘導)
# ------------------------------------------------------------
# pip は uv のセキュリティ設定（検疫期間・レジストリ固定等）を
# バイパスするため、シェルレベルで完全にブロックする。
pip() {
  echo "❌ [Harness Guard] pip コマンドの使用は全面的にブロックされています。" >&2
  echo "💡 理由: プロジェクトの依存管理およびセキュリティ設定(検疫等)は uv に完全一元化されています。" >&2
  echo "✅ 対策: AIエージェントおよびユーザーは、pip の代わりに uv を使用してください。" >&2
  echo "   - 変換例 (依存の追加)     : pip install <pkg> -> uv add <pkg>" >&2
  echo "   - 変換例 (既存環境の更新) : pip install <pkg> -> uv pip install <pkg>" >&2
  echo "   - 変換例 (ツールの導入)   : pip install <pkg> -> uv tool install <pkg>" >&2
  return 1
}

pip3() {
  pip "$@"
}

# ------------------------------------------------------------
# 3. uvx の無効化 (直接実行 / 使い捨て実行の防止)
# ------------------------------------------------------------
# uvx はパッケージをロックファイルに固定せず、ネットワークから
# 未検疫のコードを直接実行できてしまうためブロックする。
uvx() {
  echo "🚨 [Harness Guard] uvx によるリモートコードの直接実行はブロックされました。" >&2
  echo "💡 理由: 未検疫パッケージの使い捨て実行や、依存関係の隠蔽を防ぐためです。" >&2
  echo "" >&2
  echo "✅ 対策: プロジェクトに固定してから実行してください:" >&2
  echo "     1. uv add --dev <package>  # 開発用依存としてプロジェクトに追加" >&2
  echo "     2. uv run <package>        # ローカル環境で安全に実行" >&2
  return 1
}

# ------------------------------------------------------------
# 4. uv tool run の無効化 (uvx の実体コマンドのブロック)
# ------------------------------------------------------------
# uvx の実体であり、AIエージェントが迂回して使用する可能性があるためブロック。
# (Node.js における pnpm dlx のブロックと同等の防衛線)
uv() {
  if [ "$1" = "tool" ] && [ "$2" = "run" ]; then
    echo "🚨 [Harness Guard] uv tool run による直接実行はブロックされました。" >&2
    echo "💡 理由: 使い捨て実行は検疫をすり抜けるため許可されていません。" >&2
    echo "" >&2
    echo "✅ 対策: プロジェクトに固定してから実行してください:" >&2
    echo "     1. uv add --dev <package>" >&2
    echo "     2. uv run <package>" >&2
    return 1
  fi
  command uv "$@"
}

# ------------------------------------------------------------
# 5. uv ショートカット
# ------------------------------------------------------------
alias uv-config="cat ~/.config/uv/uv.toml"