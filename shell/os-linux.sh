#!/bin/sh
# ============================================================
# Linux 固有セキュリティ設定 (Harness Engineering)
#
# 目的: Linux環境(bash)における履歴を通じた機密情報漏洩の防止と、
#       パッケージ管理に対するセキュリティポリシーの明文化。
# 読込: ~/dotfiles/setup.sh により OS 判定後にロード
# ============================================================

# ------------------------------------------------------------
# 1. パッケージ管理 (apt) 防衛設定
# ------------------------------------------------------------
# ポリシー: 
# apt コマンドによるパッケージ操作はシステム全体に影響を及ぼすため、
# 常に明示的な root 権限 (sudo) での実行のみを許容する。
# 最大の防御を維持するため、本ファイルでのエイリアス化や
# 暗黙的な自動化（ラッパーの作成など）は一切実施しない。
# 
# ※ 更新作業は人間が意図して `sudo apt update && sudo apt upgrade` を
#    実行する運用とする。

# ------------------------------------------------------------
# 2. シェル履歴の防衛設定 (bash専用)
# ------------------------------------------------------------
# AIエージェントや悪意あるプロセスが履歴ファイル(~/.bash_history)から
# 機密情報を読み取るリスクを軽減する。

# スペース始まりのコマンド、および連続する重複コマンドを履歴に保存しない
export HISTCONTROL=ignoreboth

# 機密情報を含む可能性のあるコマンドを履歴から除外 (bashの区切り文字はコロン)
export HISTIGNORE="*TOKEN*:*SECRET*:*PASSWORD*:*KEY*:*API_KEY*:*AUTH*"